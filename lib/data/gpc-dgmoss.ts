export function getGPCBySpecialty(specialty: string): GPC[] {
  return GPC_DATABASE.filter((gpc) => gpc.specialty === specialty);
}

export function getGPCByFrequency(frequency: GPC["frequency"]): GPC[] {
  return GPC_DATABASE.filter((gpc) => gpc.frequency === frequency);
}

export function searchGPC(query: string): GPC[] {
  const lowerQuery = query.toLowerCase();
  return GPC_DATABASE.filter(
    (gpc) =>
      gpc.title.toLowerCase().includes(lowerQuery) ||
      gpc.code.toLowerCase().includes(lowerQuery) ||
      gpc.specialty.toLowerCase().includes(lowerQuery)
  );
}

export function getTopGPC(limit: number = 20): GPC[] {
  return GPC_DATABASE.slice(0, limit);
}
